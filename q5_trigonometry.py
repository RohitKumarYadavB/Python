# 5.Compute a)sin of 60 degree b)cos of pi c)sin(0.8660254037844386) d)tan of 90 degree
import math

print(f"Sin of 60 Degrees: {math.sin(math.radians(60))}")
print(f"Cos of Pi: {math.cos(math.radians(math.pi))}")
print(f"Sin of (0.8660254037844386): {math.sin(0.8660254037844386)}")
print(f"Tan of 90 Degrees: {math.tan(math.radians(90))}")
